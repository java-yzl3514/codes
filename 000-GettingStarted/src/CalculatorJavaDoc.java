/**
 *
 * This class represents a calculator.
 * <p>This calculator has <b>two operations :</b></p>
 * <div>
 *   <ul>
 *      <li>add</li>
 *      <li>multiply</li>
 *    </ul>
 *  </div>
 *
 *  @author Onder Sahin
 *  @version 1.0.0
 *
 */
public class CalculatorJavaDoc {


    /**
     * This method returns the addition of two integers.
     *
     * @param a the first number to add
     * @param b the second number to add
     * @return a plus b
     */
    public int add(int a, int b) {
        return a + b;
    }


    /**
     *
     * This method calculates two integer multiplication.
     *
     * @param a the first integer to multiply
     * @param b the second integer to multiply
     * @return a multiply b
     */
    public int multiply(int a, int b){
        return a * b;
    }


}
