public class StringLiterals {

    public static void main(String[] args) {


        String zeroCharString = "";

        String message = "Hello Java Strings";

        String lfMessage = "Hello\nJava\nStrings";

        String backslash = "\\";

        System.out.println(zeroCharString);
        System.out.println(message);
        System.out.println(lfMessage);
        System.out.println(backslash);
    }
}
